#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	if (fd < 0)
		return ;
	ft_putstr_fd(s, fd);
	write(fd, "\n", 1);
}

void	ft_putendl(char *s)
{
	ft_putstr(s);
	ft_putchar('\n');
}
